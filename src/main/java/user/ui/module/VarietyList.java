package user.ui.module;

import java.util.Set;

public class VarietyList {
	
	String listname;
	
	Set<String> varietySet;
	
	Integer phenotype;
	
	String varietyList;

	public String getListname() {
		return listname;
	}

	public void setListname(String listname) {
		this.listname = listname;
	}

	public Set<String> getVarietySet() {
		return varietySet;
	}

	public void setVarietySet(Set<String> varietySet) {
		this.varietySet = varietySet;
	}

	public Integer getPhenotype() {
		return phenotype;
	}

	public void setPhenotype(Integer phenotype) {
		this.phenotype = phenotype;
	}

	public String getVarietyList() {
		return varietyList;
	}

	public void setVarietyList(String varietyList) {
		this.varietyList = varietyList;
	}
	
	
	

}
